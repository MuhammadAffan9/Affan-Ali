a = 50
b = 3
print("a + b = ", a + b)  # This will add a and b
print("a - b = ", a - b)  # This will subtract b from a
print("a * b = ", a * b)  # This will multiply a and b
print("a / b = ", a / b)  # This will divide a by b
print("a // b = ", a // b)  # This will perform floor division of a by b
print("a % b = ", a % b)  # This will give the remainder of a divided by b
print("a ** b = ", a ** b)  # This will raise a to the power of b

print(f"50 + 3 = {a + b}")
print(f"50 - 3 = {a - b}")
print(f"50 * 3 = {a * b}")
print(f"50 / 3 = {a / b}")
print(f"50 // 3 = {a // b}")
print(f"50 % 3 = {a % b}")
print(f"50 ** 3 = {a ** b}")